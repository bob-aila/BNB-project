      <h1 id="font">AIR BNB</h1><br>
      <p class="text-justify"> Airbnb: An Overview<br>

Airbnb is an online marketplace that connects people who want to rent out their homes with people who are looking for accommodations in that locale. It currently covers more than 100,000 cities and 220 countries worldwide. The company’s name comes from “air mattress B&B.”<p align="left">

For hosts, participating in Airbnb is a way to earn some income from their property, but with the risk that the guest might do damage to it. For guests, the advantage can be relatively inexpensive accommodations, but with the risk that the property won’t be as appealing as the listing made it seem.
<p align="left">
<b>Key Takeaways</b>
<p align="left">
 <ol>
    <li>Travelers can often book an Airbnb for less than the cost of a hotel room.</li>
    <li>The main risk to the traveler is that the property may not live up to its listing.</li>
    <li>The main risk to hosts is that guests might do serious damage to their property.</li>
    <li>The global pandemic due to the novel coronavirus has resulted in significant changes to Airbnb’s extenuating circumstances cancellation policy.</li>
    <hr>
</ol>
</p>
<h1 align="left"id="font">Services</h1><br>
<p class="text-justify" align="left"><b>For Hosts</b><br>
If you have a room to rent (or an entire house), you will have to register your listing on Airbnb and outline certain specifications like area, size, number of rooms, and other details on your listing. You will create a profile, add a profile picture, and go through a series of verification and scanning processes (including verifying your phone number and verifying your ID).<p align="left">

However, before you proceed to listing your place, you will need to check out the legality of listing a rental in your city or neighborhood. Some areas have various laws on renting out spaces in a home for extended periods of time, so it is vital to check and ensure your listing on Airbnb is perfectly legal.<br> The site provides a "Your City's Regulations" page that can help you determine if listing your place is appropriate.<br>
 Additionally, you may need to alert your HOA or other such boards about your Airbnb listing. 

Once your profile is set, you can then go to the "List Your Space" section, which will prompt you to describe what kind of lodging you are listing - whether it's an apartment, house, room, or other. Additionally, you will be prompted to specify your location, how many guests your listing can accommodate, and availability.<br> You will be able to set your own listing price per night, week, or month.<p align="left">

And, if you need photos of your space but aren't especially photographically inclined, Airbnb provides free photography (if your area qualifies) to help you make your place look its best (and get you the best price). <p align="left">

Still, guest approval is always up to the host - and once you get a request to book your listing, you will be able to deal directly with the guest. To make the transaction as easy as possible, it is often helpful to include expectations or specifications about your listing that might be helpful for guests to know - like any quiet hours you might have, cleanliness standards, or even how many flights of stairs you have. You are also able to chat with potential guests and even write 500-word reviews (as both host and guest). 

Once you have your listing set up, Airbnb provides a variety of services including 24/7 support, "rebooking assistance, refunds, reimbursements, [a] 1 million Host Guarantee, and insurance programs for both homes and experiences," as well as safety services like carbon monoxide and smoke detectors, fire extinguishers, and first aid kits.<br>

When you've approved a guest, Airbnb handles the payments but holds onto the host's payment until 24 hours after check in. Additionally, hosts should thoroughly read Airbnb's community standards. <br>

Payment options vary depending on country, but you can typically be paid through PayPal, wire transfer, or direct deposit (with a 3% servicing fee).</div>
 
<hr>
<p class="text-justify" align="left"><b>For Guests</b><br>
Airbnb makes it easy and inexpensive to find the best deal for your destination. </p>
<p align="left">
As a guest, you can go on the Airbnb website and search through dozens of filters like area (city, country), space (if you need a room or house), and number of guests to accommodate.
<p align="left">
To get started browsing listings, you need to create a profile on Airbnb and provide information and go through verification like a profile picture and ID. Once you've completed and filled in basic information, you can browse over 800,000 listings worldwide. Make sure you put the correct dates and number of guests to ensure you are getting the best price and that your trip will go smoothly. Additionally, take advantage of Airbnb's contact program to speak directly with the host and work out details or get any questions answered before booking. 
